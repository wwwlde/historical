stk segment stack 'stack'
    dw 64 dup (?)      ; Выделяем 64 байта для стека
stk ends

;---------------------------------------------------------------
; Сегмент данных
;---------------------------------------------------------------
dat segment 'data'
    x db ?             ; Переменная для данных
dat ends

;---------------------------------------------------------------
; Сегмент кода
;---------------------------------------------------------------
cd segment 'code'
    assume cs:cd, ds:dat, ss:stk

begin:
    ; Инициализация сегмента данных
    mov ax, dat
    mov ds, ax         ; Загружаем адрес сегмента данных в ds

    ; Инициализация мыши
    xor ax, ax         ; Очищаем регистры
    int 33h            ; Вызываем прерывание для инициализации мыши

    ; Переход в режим получения координат мыши (если нужно)
    mov ax, 0003h      ; Запросим положение мыши
    int 33h            ; Вызов прерывания для получения координат мыши

    ; Завершаем программу
    mov ah, 4Ch        ; Функция завершения программы
    int 21h            ; Вызов прерывания для завершения программы

cd ends
end begin
